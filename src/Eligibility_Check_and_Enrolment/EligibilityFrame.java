    // helper: find Student object by ID
    private EligibilityStudent findStudentById(String studentID) {
    for (EligibilityStudent s : students) {
        if (studentID != null && studentID.equals(s.getStudentID())) {
            return s;
        }
    }
    return null;
}

    // helper: rewrite StudentDB.txt updating the year field for students we have in memory
    private void saveStudentsToFile() {
    java.io.File file = new java.io.File("data/StudentDB.txt");
    java.util.List<String> outputLines = new java.util.ArrayList<>();

    try (java.io.BufferedReader br = new java.io.BufferedReader(new java.io.FileReader(file))) {
        String header = br.readLine();
        if (header != null) {
            outputLines.add(header); // keep header
        }
        String line;
        while ((line = br.readLine()) != null) {
            if (line.trim().isEmpty()) {
                outputLines.add(line);
                continue;
            }
            String[] parts = line.split(",", -1); // keep empty tokens
            if (parts.length < 6) {
                // malformed line — keep as-is
                outputLines.add(line);
                continue;
            }
            String studentID = parts[0].trim();
            EligibilityStudent s = findStudentById(studentID);
            if (s != null) {
                // Replace the year field (index 4) with the current year text
                parts[4] = s.getYearText();
                outputLines.add(String.join(",", parts));
            } else {
                outputLines.add(line);
            }
        }
    } catch (java.io.IOException ex) {
        logger.log(java.util.logging.Level.SEVERE, "Error reading StudentDB.txt for saving", ex);
        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Failed to read StudentDB.txt while saving. Changes were not saved.",
                "Save Error",
                javax.swing.JOptionPane.ERROR_MESSAGE
        );
        return;
    }

    // Write back (overwrite)
    try (java.io.PrintWriter pw = new java.io.PrintWriter(new java.io.FileWriter(file, false))) {
        for (String l : outputLines) {
            pw.println(l);
        }
    } catch (java.io.IOException ex) {
        logger.log(java.util.logging.Level.SEVERE, "Error writing StudentDB.txt", ex);
        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Failed to write StudentDB.txt. Changes were not saved.",
                "Save Error",
                javax.swing.JOptionPane.ERROR_MESSAGE
        );
    }
}
    
    private void enrol() {
        // 1) Check if a row is selected
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            // No row selected
            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Please select a student first.",
                    "No Student Selected",
                    javax.swing.JOptionPane.WARNING_MESSAGE
            );
            return; // stop here
        }

        // 2) Get the Student object that matches this row
        EligibilityStudent selectedStudent = students.get(selectedRow);

        // 3) Check eligibility using your lecturer's rule
        if (!selectedStudent.isEligible()) {
            // Not eligible -> show error page (dialog)
            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "This student does not meet the criteria for progression.\n"
                  + "Please select another student.",
                    "Enrollment Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE
            );
            return; // stop here
        }

        // 4) Eligible -> ask for confirmation
        int choice = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "You are about to enroll this student to the next level.\n"
              + "Are you sure you want to proceed?",
                "Confirm Enrollment",
                javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (choice != javax.swing.JOptionPane.YES_OPTION) {
            // User clicked NO -> do nothing
            return;
        }

        // 5) Progress the student (move level or mark Graduated)
        selectedStudent.progress();
        saveStudentsToFile();
        loadTable();

        // (Optional) If you later show level/status in table, you would call:
        // loadTable();

        // 6) Show success message
        javax.swing.JOptionPane.showMessageDialog(
                this,
                "You have successfully enrolled this student.",
                "Enrollment Successful",
                javax.swing.JOptionPane.INFORMATION_MESSAGE
        );
    
        StudentService ss = new StudentService();
        // Get the array of all rows from StudentDB
        Classes.Student[] student = ss.loadData();
        
        // Call Email Service
        EmailService es = new EmailService();
        
        // To store recepient email
        String recipientEmail = "";
        
        // Loop through every row in student array
        for(int i = 0; i < student.length; i++) {
            // Check which row matches the studentID that is passed in
            if(student[i].getStudentID().equals(selectedStudent.getStudentID())) {
                // Get the student email
                recipientEmail = student[i].getEmail();
            }
        }
        
        // Set subject for email header
        String subject = "Your New Academic Year Is Now Active";
        // Set text message for the email body
        String textMessage = "Dear Student,\n\n"
                + "You have been successfully enrolled into the new year. Please log in to your APSpace to check your updated timetable.\n\n"
                + "If you have any questions, feel free to reach out.\n\n"
                + "Best regards";
        
        es.joinEmail(recipientEmail, subject);
        // Call sendMail method in JavaMailUtil to send notification to the student
        JavaMailUtil.sendMail(recipientEmail, subject, textMessage);
    }
    
