     // Load real students from CourseDB.txt, StudentDB.txt and StudentGradeDB.txt
    private void loadStudentsFromFiles() {
    students.clear();

    // ---------- STEP 1: Load course credits from CourseDB ----------
    // courseID -> credits
    Map<String, Integer> courseCredits = new HashMap<>();

    try (BufferedReader br = new BufferedReader(new FileReader("src/Eligibility_Check_and_Enrolment/CourseDB.txt"))) {
        String line = br.readLine(); // header: courseID,courseName,credits,...
        while ((line = br.readLine()) != null) {
            line = line.trim();
            if (line.isEmpty() || line.startsWith("...")) {
                continue;
            }

            String[] parts = line.split(",");
            if (parts.length < 3) {
                continue;
            }

            String courseID = parts[0].trim();
            int credits;

            try {
                credits = Integer.parseInt(parts[2].trim());
            } catch (NumberFormatException e) {
                continue;
            }

            courseCredits.put(courseID, credits);
        }
    } catch (IOException ex) {
        logger.log(java.util.logging.Level.SEVERE, "Error reading CourseDB.txt", ex);
        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Error loading CourseDB.txt. Please make sure the file exists.",
                "File Error",
                javax.swing.JOptionPane.ERROR_MESSAGE
        );
        return;
    }

    // ---------- STEP 2: Load basic student info from StudentDB ----------
    // studentID -> Student object (name + level + status)
    Map<String, EligibilityStudent> studentMap = new HashMap<>();

    try (BufferedReader br = new BufferedReader(new FileReader("src/Eligibility_Check_and_Enrolment/StudentDB.txt"))) {
        String line = br.readLine(); // header: studentID,firstName,lastName,major,year,email
        while ((line = br.readLine()) != null) {
            line = line.trim();
            if (line.isEmpty() || line.startsWith("...")) {
                continue;
            }

            String[] parts = line.split(",");
            if (parts.length < 6) {
                continue;
            }

            String studentID = parts[0].trim();
            String firstName = parts[1].trim();
            String lastName  = parts[2].trim();
            String yearText  = parts[4].trim(); // Freshman / Sophomore / Junior / Senior
            // we ignore major + email for this screen

            String fullName = firstName + " " + lastName;
            int level = convertYearToLevel(yearText);

            // cgpa & fails will be filled later
            EligibilityStudent s = new EligibilityStudent(studentID,fullName, level);
            studentMap.put(studentID, s);
        }
    } catch (IOException ex) {
        logger.log(java.util.logging.Level.SEVERE, "Error reading StudentDB.txt", ex);
        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Error loading StudentDB.txt. Please make sure the file exists.",
                "File Error",
                javax.swing.JOptionPane.ERROR_MESSAGE
        );
        return;
    }

    // ---------- STEP 3: Process StudentGradeDB to compute CGPA + fails ----------
    // We accumulate:
    //   totalGradePoints[studentID] = sum(gradePoint * credits)
    //   totalCredits[studentID]     = sum(credits)
    //   failedCounts[studentID]     = number of 'F' grades

    Map<String, Double> totalGradePoints = new HashMap<>();
    Map<String, Integer> totalCredits = new HashMap<>();
    Map<String, Integer> failedCounts = new HashMap<>();

    try (BufferedReader br = new BufferedReader(new FileReader("src/Eligibility_Check_and_Enrolment/StudentGradeDB.txt"))) {
        String line = br.readLine(); // header: gradeID,studentID,courseID,year,gradePoint,grade
        while ((line = br.readLine()) != null) {
            line = line.trim();
            if (line.isEmpty() || line.startsWith("...")) {
                continue;
            }

            String[] parts = line.split(",");
            if (parts.length < 6) {
                continue;
            }

            String gradeID   = parts[0].trim(); // unused, just for info
            String studentID = parts[1].trim();
            String courseID  = parts[2].trim();
            String yearText  = parts[3].trim(); // not used here
            String gradePointStr = parts[4].trim();
            String letterGrade   = parts[5].trim(); // e.g. A, B+, C-, F

            // Only process if we know this student and this course exists
            if (!studentMap.containsKey(studentID)) {
                continue;
            }
            if (!courseCredits.containsKey(courseID)) {
                continue; // no credits info → skip
            }

            int credits = courseCredits.get(courseID);
            double gradePoint;
            try {
                gradePoint = Double.parseDouble(gradePointStr);
            } catch (NumberFormatException e) {
                continue;
            }

            // total grade points = sum(gradePoint * credits)
            double currentTGP = totalGradePoints.getOrDefault(studentID, 0.0);
            totalGradePoints.put(studentID, currentTGP + gradePoint * credits);

            // total credits = sum(credits)
            int currentCredits = totalCredits.getOrDefault(studentID, 0);
            totalCredits.put(studentID, currentCredits + credits);

            // failed course: letterGrade == "F"
            // failed course if gradePoint < 2.0
            if (gradePoint < 2.0) {
                int currentFails = failedCounts.getOrDefault(studentID, 0);
                failedCounts.put(studentID, currentFails + 1);
            }
        }
    } catch (IOException ex) {
        logger.log(java.util.logging.Level.SEVERE, "Error reading StudentGradeDB.txt", ex);
        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Error loading StudentGradeDB.txt. Please make sure the file exists.",
                "File Error",
                javax.swing.JOptionPane.ERROR_MESSAGE
        );
        return;
    }

    // ---------- STEP 4: Calculate CGPA + failed courses and fill Student list ----------
    for (String studentID : studentMap.keySet()) {
        EligibilityStudent s = studentMap.get(studentID);

        double tgp = totalGradePoints.getOrDefault(studentID, 0.0);
        int tch    = totalCredits.getOrDefault(studentID, 0);
        int fails  = failedCounts.getOrDefault(studentID, 0);

        double cgpa = 0.0;
        if (tch > 0) {
            cgpa = tgp / tch;
        }

        s.setCgpa(cgpa);
        s.setFailedCourses(fails);

        students.add(s);
    }
}
    
        // Convert academic year text from StudentDB to a numeric level
    // 1 = Freshman, 2 = Sophomore, 3 = Junior, 4 = Senior
    private int convertYearToLevel(String year) {
        if (year == null) {
            return 1;
        }
        year = year.trim().toLowerCase();
        switch (year) {
            case "freshman":
                return 1;
            case "sophomore":
                return 2;
            case "junior":
                return 3;
            case "senior":
                return 4;
            default:
                return 1; // safe default if something weird is in the file
        }
    }
    
